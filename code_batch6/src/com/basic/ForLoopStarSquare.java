package com.basic;



public class ForLoopStarSquare {

	public static void main(String[] args) {
		int i, j, a;
	      a = 12;
	      System.out.println("The Square length  " +a);
	      System.out.println("The square pattern : ");
	      for(i = 1; i <= a; i++){
	         for(j = 1; j <= a; j++){
	            System.out.print(" * ");
	         }
	         System.out.print("\n");
	      }
	   }
	}

	

