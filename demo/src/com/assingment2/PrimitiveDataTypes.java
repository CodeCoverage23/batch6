package com.assingment2;

//1 .  Write the Java Program in which declares the different primitives data types.

public class PrimitiveDataTypes {
	
	boolean b = true;
	char c = 'C';
	byte a = -13;
	short s = -32000;
	long l = 349099999; //it consist 9 digit.
	int x = 895439878; //it consist 9 digit.
	float f = 10.3545f;
	double d = 101.101;
	
	public static void main(String[] args) {
		PrimitiveDataTypes n = new PrimitiveDataTypes();
		System.out.println(n.b);
		System.out.println(n.c);
		System.out.println(n.a);
		System.out.println(n.s);
		System.out.println(n.l);
		System.out.println(n.x);
		System.out.println(n.f);
		System.out.println(n.d);
	}

}
