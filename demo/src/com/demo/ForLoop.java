package com.demo;

public class ForLoop {

	public static void main(String[] args) {

		/*
		 * for(int i=0;i<10;i++) { System.out.println(i); }
		 */
		int a, b, c, d, e, f, g, h, j;

		for (int i = 1; i <= 10; i++)

		{
	
			a = 2 * i;
			b = 3 * i;
			c = 4 * i;
			d = 5 * i;
			e = 6 * i;
			f = 7 * i;
			g = 8 * i;
			h = 9 * i;
			j = 10 * i;

			System.out.println(a + "     " + b + "    " + c + "    " + d + "    " + e + "  " + f + "   " + g + "    "
					+ h + " " + j);
			
			
		}

	}

}
